package com.spring.AtoZ.dashboard.dto;

public class OrderScheduleCommand {
	private String deli_ymd;
	private String co_code;
	private int ord_no;
	private String item_name;
	public int getOrd_no() {
		return ord_no;
	}
	@Override
	public String toString() {
		return "OrderScheduleCommand [deli_ymd=" + deli_ymd + ", co_code=" + co_code + ", ord_no=" + ord_no
				+ ", item_name=" + item_name + "]";
	}
	public void setOrd_no(int ord_no) {
		this.ord_no = ord_no;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getDeli_ymd() {
		return deli_ymd;
	}
	public void setDeli_ymd(String deli_ymd) {
		this.deli_ymd = deli_ymd;
	}
	public String getCo_code() {
		return co_code;
	}
	public void setCo_code(String co_code) {
		this.co_code = co_code;
	}
	
	
}
