package com.spring.AtoZ.contract.dto;

public class DetailAreaCommand {
	
	private int used_area;
	private int tot_area;
	private int can_area;
	private String cm_name;
	private String cm_code;
	private int wz_no; 
		
	public int getWz_no() {
		return wz_no;
	}
	public void setWz_no(int wz_no) {
		this.wz_no = wz_no;
	}
	public int getUsed_area() {
		return used_area;
	}
	public void setUsed_area(int used_area) {
		this.used_area = used_area;
	}
	public int getTot_area() {
		return tot_area;
	}
	public void setTot_area(int tot_area) {
		this.tot_area = tot_area;
	}
	public int getCan_area() {
		return can_area;
	}
	public void setCan_area(int can_area) {
		this.can_area = can_area;
	}
	public String getCm_name() {
		return cm_name;
	}
	public void setCm_name(String cm_name) {
		this.cm_name = cm_name;
	}
	public String getCm_code() {
		return cm_code;
	}
	public void setCm_code(String cm_code) {
		this.cm_code = cm_code;
	}
	
	

}
