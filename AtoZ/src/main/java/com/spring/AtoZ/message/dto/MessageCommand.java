package com.spring.AtoZ.message.dto;

public class MessageCommand {
	private String msg_no;
	private String sd_code;
	private String rc_code;
	private String content;
	private String wrt_ymd;
	private String read_yn;
	private String sd_del_gb;
	private String rc_del_gb;
	private String sd_name;
	private String rc_name;
	@Override
	public String toString() {
		return "MessageCommand [msg_no=" + msg_no + ", sd_code=" + sd_code + ", rc_code=" + rc_code + ", content="
				+ content + ", wrt_ymd=" + wrt_ymd + ", read_yn=" + read_yn + ", sd_del_gb=" + sd_del_gb
				+ ", rc_del_gb=" + rc_del_gb + ", sd_name=" + sd_name + ", rc_name=" + rc_name + "]";
	}
	public String getMsg_no() {
		return msg_no;
	}
	public void setMsg_no(String msg_no) {
		this.msg_no = msg_no;
	}
	public String getSd_code() {
		return sd_code;
	}
	public void setSd_code(String sd_code) {
		this.sd_code = sd_code;
	}
	public String getRc_code() {
		return rc_code;
	}
	public void setRc_code(String rc_code) {
		this.rc_code = rc_code;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getWrt_ymd() {
		return wrt_ymd;
	}
	public void setWrt_ymd(String wrt_ymd) {
		this.wrt_ymd = wrt_ymd;
	}
	public String getRead_yn() {
		return read_yn;
	}
	public void setRead_yn(String read_yn) {
		this.read_yn = read_yn;
	}
	public String getSd_del_gb() {
		return sd_del_gb;
	}
	public void setSd_del_gb(String sd_del_gb) {
		this.sd_del_gb = sd_del_gb;
	}
	public String getRc_del_gb() {
		return rc_del_gb;
	}
	public void setRc_del_gb(String rc_del_gb) {
		this.rc_del_gb = rc_del_gb;
	}
	public String getSd_name() {
		return sd_name;
	}
	public void setSd_name(String sd_name) {
		this.sd_name = sd_name;
	}
	public String getRc_name() {
		return rc_name;
	}
	public void setRc_name(String rc_name) {
		this.rc_name = rc_name;
	}
}
